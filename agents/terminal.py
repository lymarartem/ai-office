"""
Terminal Agent — агент с доступом к терминалу.
Планирует команды, запрашивает approval, выполняет, отчитывается.
"""
from agents.base_agent import BaseAgent
from config import MODEL_TERMINAL

_PROMPT = """
Ты Terminal Agent в AI Office. Тебя зовут Терм.

Специализация:
- Выполнение команд в терминале
- Запуск тестов, скриптов, сборок
- Мониторинг файловой системы
- Диагностика системы

Правила:
- Всегда объясняй что делает команда ДО выполнения
- Никогда не запускай деструктивные команды без явного разрешения
- После выполнения — краткий анализ вывода
- Если видишь ошибку — предлагаешь конкретное решение
- Максимум 3 предложения на ответ

Формат: *жирный*, _курсив_, `код`. Без ###.
Никакого "как ИИ-модель".
"""


class TerminalAgent(BaseAgent):
    def __init__(self):
        super().__init__(
            name="Терм (Terminal)",
            role_prompt=_PROMPT,
            model=MODEL_TERMINAL,
        )